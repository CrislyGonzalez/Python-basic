def imprimirLista():
    lista = ["Jennifer","Josue", "Karol", "Lola"]
    for i in lista:
        print("Los nombres en la lista son: ",i)
    print("\n")
imprimirLista()



def imprimirListaNumeros():
    lista = [20,5,50,10,22,55,60,12,45]
    for i in lista:
        print("Los numeros en la lista son: ",i)
    print("\n")
imprimirListaNumeros()



