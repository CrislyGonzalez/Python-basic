
#Eejemplos de Operadores lógicos, se harán con cada uno de los operandos

# a==b
def OpIgual():
    if(5 == 5):
        print("Son numeros iguales")

    else:
        print("No son numeros iguales")

# a!= b
def OpDiferente():
    if(5 != 5):
        print("Son numeros iguales")

    else:
        print("Son numeros diferentes")

# a>b
def OpMayorQ():
    if(5 > 6):
        print("Correcto, el primer numero es mayor")

    else:
        print("Correcto, el segundo numero es mayor")


# a<b
def OpMenorQ():
    if(5 < 6):
       print("Correcto, el primer numero es menor")

    else:
        print("Correcto, el primer numero es mayor")


# a>=b
def OpMayorI():
    if(5 >= 6):
        print("Correcto, el primer numero es mayor")

    else:
        print("Correcto, el segundo numero es mayor")


# a<=b
def OpMenorI():
    if(5 <= 6):
       print("Correcto, el primer numero es menor")

    else:
        print("Correcto, el primer numero es mayor")